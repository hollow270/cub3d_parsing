#include "../../inc/cub3d.h"

int	check_color_data(t_assets *a)
{
	if (a->c_rgb[0] == -1 || a->c_rgb[1] == -1 || a->c_rgb[2] == -1)
		return (0);
	else if (a->f_rgb[0] == -1 || a->f_rgb[1] == -1 || a->f_rgb[2] == -1)
		return (0);
	return (1);
}
